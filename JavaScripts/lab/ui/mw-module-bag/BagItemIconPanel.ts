export class BagItem implements IUnique {
    public id: number;
    public name: string;
    public desc: string;
    public count: number;

    constructor(id: number,
                name: string,
                desc: string,
                count: number) {
        this.id = id;
        this.name = name;
        this.desc = desc;
        this.count = count;
    }

    public move(updated: this): boolean {
        this.id = updated.id;
        this.name = updated.name;
        this.desc = updated.desc;
        this.count = updated.count
        ;
        return false;
    }

    public primaryKey(): number {
        return this.id;
    }
}

