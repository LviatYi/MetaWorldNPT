export class FakeTransform {
    public _position: mw.Vector | undefined;

    public set position(pos: mw.Vector | undefined) {
        if (this._position) this._position.set(pos ?? mw.Vector.zero);
        else this._position = pos;
    }

    public get position(): mw.Vector | undefined {
        if (this.parent) return this._position?.clone().add(this.parent.worldTransform.position) ?? undefined;
        else return this._position?.clone() ?? undefined;
    }

    constructor(position: mw.Vector | undefined, public parent?: mw.GameObject) {
        this._position = position;
    }
}